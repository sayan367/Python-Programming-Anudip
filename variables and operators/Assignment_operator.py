# Initial value
x = 10

# Addition assignment
x += 5  # Equivalent to x = x + 5
print(f"x after addition assignment: {x}")

# Subtraction assignment
x -= 3  # Equivalent to x = x - 3
print(f"x after subtraction assignment: {x}")

# Multiplication assignment
x *= 2  # Equivalent to x = x * 2
print(f"x after multiplication assignment: {x}")

# Division assignment
x /= 4  # Equivalent to x = x / 4
print(f"x after division assignment: {x}")

# Modulus assignment
x %= 3  # Equivalent to x = x % 3
print(f"x after modulus assignment: {x}")

# Exponentiation assignment
x **= 2  # Equivalent to x = x ** 2
print(f"x after exponentiation assignment: {x}")

# Floor division assignment
x //= 2  # Equivalent to x = x // 2
print(f"x after floor division assignment: {x}")
